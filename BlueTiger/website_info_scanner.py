def get_website_info(url):
    print(f"{blue}Fetching information for {url}...{Style.RESET_ALL}")
    try:
        response = requests.get(url)
        print(f"{blue}Status Code: {response.status_code}{Style.RESET_ALL}")
        print(f"{blue}Headers:{Style.RESET_ALL}")
        for key, value in response.headers.items():
            print(f"{key}: {value}")
    except requests.exceptions.RequestException as e:
        print(f"{red}Error: {e}{Style.RESET_ALL}")

def get_whois_info(domain):
    print(f"{blue}Fetching WHOIS information for {domain}...{Style.RESET_ALL}")
    try:
        w = whois.whois(domain)
        print(f"{blue}WHOIS Information:{Style.RESET_ALL}")
        for key, value in w.items():
            print(f"{key}: {value}")
    except Exception as e:
        print(f"{red}Error: {e}{Style.RESET_ALL}")

