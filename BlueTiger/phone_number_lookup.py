def phone_number_lookup(phone_number):
    """
    Führt eine grundlegende Telefonnummern-Lookup durch.
    """
    try:
        parsed_number = phonenumbers.parse(phone_number)

        print(f"\nAnalyse für Telefonnummer: {phonenumbers.format_number(parsed_number, phonenumbers.PhoneNumberFormat.INTERNATIONAL)}")

        # Geografische Region
        region = geocoder.description_for_number(parsed_number, "de") # 'de' für deutsche Beschreibung
        if region:
            print(f"  Geografische Region: {region}")
        else:
            print("  Geografische Region: Nicht gefunden.")

        # Mobilfunkanbieter (kann ungenau oder nicht verfügbar sein)
        service_provider = carrier.name_for_number(parsed_number, "de")
        if service_provider:
            print(f"  Mobilfunkanbieter: {service_provider}")
        else:
            print("  Mobilfunkanbieter: Nicht gefunden.")

        # Gültigkeit der Nummer
        if phonenumbers.is_valid_number(parsed_number):
            print("  Die Telefonnummer ist gültig.")
        else:
            print("  Die Telefonnummer ist ungültig.")

    except phonenumbers.NumberParseException as e:
        print(f"Fehler beim Parsen der Telefonnummer: {e}")

if __name__ == "__main__":
    phone = input("Gib die Telefonnummer ein (mit Ländercode, z.B. +49176...): ")
    phone_number_lookup(phone)